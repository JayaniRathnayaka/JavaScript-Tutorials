//FOR LOOP
for(let i=0; i<12; i++){
    console.log("Number" + i);
    
    
}
console.log(" ");


// WHILE LOOP
var count = 1;

while(count<=5){
    console.log("Number" + count)
    count++;
}
console.log(" ");


//DO-WHILE LOOP
let num = 1;

do{
    console.log("Num is:" + num)
    num++;
}while(num<=5);